/**
 * Esta clase proporciona métodos para realizar operaciones matemáticas simples
 * utilizando la clase Math de Java.
 * * @author David
 * @version 1.0
 */

public class miniCalculadora {

    /**
     * Calcula el valor absoluto de un número decimal.
     * * @param numero El número del cual se desea obtener el valor absoluto.
     * @return El valor absoluto del número proporcionado.
     */

    public static double valorAbsoluto(double numero) {
        return Math.abs(numero);
    }

    public static double raizCuadrada(double numero) {
        return Math.sqrt(numero);
    }

}
